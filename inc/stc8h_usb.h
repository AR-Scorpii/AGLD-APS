// sfr       USBCLK      =           0xdc;
// sfr       USBDAT      =           0xec;
// sfr       USBCON      =           0xf4;
// sfr       USBADR      =           0xfc;
// SFR(USBCLK, 0xDC);
// SFR(USBDAT, 0xEC);
// SFR(USBCON, 0xF4);
// SFR(USBADR, 0xFC);

// usage: 
//      char dat;
//
//      READ_USB(CSR0, dat);        // read USB register
//      WRITE_USB(FADDR, 0x00);     // write USB register

#define     READ_USB(reg, dat)          \
            {                           \
                while (USBADR & 0x80);  \
                USBADR = (reg) | 0x80;  \
                while (USBADR & 0x80);  \
                (dat) = USBDAT;         \
            }

#define     WRITE_USB(reg, dat)         \
            {                           \
                while (USBADR & 0x80);  \
                USBADR = (reg) & 0x7f;  \
                USBDAT = (dat);         \
            }

